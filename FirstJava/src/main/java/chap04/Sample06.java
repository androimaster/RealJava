package chap04;

public class Sample06 {

    public static void main(String[] args) {
        int   x = 1;
        float y = 2.0f;

        System.out.println(x < y); //true
        System.out.println(x > y); //false
        System.out.println(x <= y);//true
        System.out.println(x >= y);//false
        System.out.println(x == y);//false
        System.out.println(x != y);//true
    }

}
